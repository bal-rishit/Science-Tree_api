router.get("/", async (req, res) => {
  const qNew = req.query.new;
  const qCategory = req.query.category;
  try {
    let products;

    if (qNew) {
      products = await find().sort({ createdAt: -1 }).limit(1);
    } else if (qCategory) {
      products = await find({
        categories: {
          $in: [qCategory],
        },
      });
    } else {
      products = await find();
    }

    res.status(200).json(products);
  } catch (err) {
    res.status(500).json(err);
  }
});

